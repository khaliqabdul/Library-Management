const { Schema, model } = require("mongoose");

const contactUsSchema = new Schema({
  fullName: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },
  query: {
    type: String,
    required: true,
    trim: true,
  },
  date: {
    type: Date,
    required: true,
    default: Date.now,
  },
});

const ContactUs = new model("ContactUs", contactUsSchema);
module.exports = ContactUs;
